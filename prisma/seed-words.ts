async function seedPronunciationWords() {
  console.log('ðŸŒ± Starting to seed pronunciation words...');

  try {
    // Loáº¡i bá» cÃ¡c tá»« trung láº·p vÃ  chuyá»ƒn vá» lowercase
    const uniqueWords = [
      ...new Set(commonWords.map((word) => word.toLowerCase())),
    ];

    console.log(`ðŸ“Š Total unique words to seed: ${uniqueWords.length}`);

    // Chia thÃ nh cÃ¡c batch Ä‘á»ƒ trÃ¡nh timeout
    const batchSize = 100;
    const batches = [];

    for (let i = 0; i < uniqueWords.length; i += batchSize) {
      batches.push(uniqueWords.slice(i, i + batchSize));
    }

    console.log(`ðŸ“¦ Processing ${batches.length} batches...`);

    let totalCreated = 0;
    let totalSkipped = 0;

    for (let i = 0; i < batches.length; i++) {
      const batch = batches[i];
      console.log(
        `â³ Processing batch ${i + 1}/${batches.length} (${batch.length} words)...`,
      );

      try {
        // Sá»­ dá»¥ng createMany vá»›i skipDuplicates Ä‘á»ƒ trÃ¡nh lá»—i unique constraint
        const result = await prisma.pronunciationWord.createMany({
          data: batch.map((word) => ({ word })),
          skipDuplicates: true,
        });

        totalCreated += result.count;
        totalSkipped += batch.length - result.count;

        console.log(
          `âœ… Batch ${i + 1} completed: ${result.count} created, ${batch.length - result.count} skipped`,
        );

        // Delay nhá» giá»¯a cÃ¡c batch Ä‘á»ƒ trÃ¡nh overwhelm database
        await new Promise((resolve) => setTimeout(resolve, 100));
      } catch (error) {
        console.error(`âŒ Error in batch ${i + 1}:`, error);
        // Continue vá»›i batch tiáº¿p theo thay vÃ¬ dá»«ng hoÃ n toÃ n
      }
    }

    // Thá»‘ng kÃª cuá»‘i cÃ¹ng
    const totalWordsInDb = await prisma.pronunciationWord.count();

    console.log('ðŸŽ‰ Seeding completed!');
    console.log(`ðŸ“ˆ Statistics:`);
    console.log(`   - Words created: ${totalCreated}`);
    console.log(`   - Words skipped (already exist): ${totalSkipped}`);
    console.log(`   - Total words in database: ${totalWordsInDb}`);
  } catch (error) {
    console.error('ðŸ’¥ Error during seeding:', error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

